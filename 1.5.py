ans_1 = -3 * 2
print(f'-3의 2배의 값을 출력하시오. : {ans_1}')

ans_2 = 3 ** 2
print(f'3의 제곱 값을 출력하시오. : {ans_2}')

ans_3_div = ans_2 // -ans_1
ans_3_mod = ans_2 % -ans_1
print(f'3의 제곱 값을 3의 2배의 값으로 나눈 몫 : {ans_3_div}')
print(f'3의 제곱 값을 3의 2배의 값으로 나눈 나머지 : {ans_3_mod}')


ans_4 = 3 ** 2 + (-3) ** 2
print(f'3의 제곱 값에 -3의 제곱 값을 더하고 출력하시오. : {ans_4}')